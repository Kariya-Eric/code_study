"""
运算符 = - * /
除的商一定是浮点数,无论能否整除,且除数不能为0
整除 // 取商的整数部分
取余 %
取幂 **
运算符的优先级：幂 > 乘除,取余，取整数 > 加减
+= : num += 1 ---> num1 = num1 + 1
-= : num -= 1 ---> num1 = num1 - 1
...
同理包括所有运算符
"""
num1 = 13 / 3
print(num1)
num2 = 13 // 3
print(num2)
# 取余数 13 % 3 = 4 余 1
num3 = 13 % 3
print(num3)
# 2的8次方
num4 = 2 ** 8
print(num4)

print(2 * 2 ** 4 / 3 + 6 // 4)
