"""
使用print函数进行简单输出
"""
a = 100
b = 50
# 输出固定值
print(90)
# 输出变量的值
print(a)
# 输出的运算结果
print(a + b)
# 输出字符串
print("hello world")
print('hello world')
print("""hello world""")
# 一次输出多条内容不换行,使用逗号分隔
print(a, b, a + b, "hello world")
# ascii和unicode码转换
print(ord('桂'))
print(chr(26690))
# 单行注释
"""
多行注释
"""
# 字符串
name = """
多行注释
"""
print(name)
